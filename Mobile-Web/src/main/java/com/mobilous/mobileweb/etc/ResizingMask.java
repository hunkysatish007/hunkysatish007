package com.mobilous.mobileweb.etc;

public class ResizingMask {

	private boolean flexibleLeftMargin = false;
	private boolean flexibleWidth = false;
	private boolean flexibleRightMargin = false;
	private boolean flexibleTopMargin = false;
	private boolean flexibleHeight = false;
	private boolean flexibleBottomMargin = false;

	public boolean isFlexibleLeftMargin() {
		return flexibleLeftMargin;
	}

	public void setFlexibleLeftMargin(boolean flexibleLeftMargin) {
		this.flexibleLeftMargin = flexibleLeftMargin;
	}

	public boolean isFlexibleWidth() {
		return flexibleWidth;
	}

	public void setFlexibleWidth(boolean flexibleWidth) {
		this.flexibleWidth = flexibleWidth;
	}

	public boolean isFlexibleRightMargin() {
		return flexibleRightMargin;
	}

	public void setFlexibleRightMargin(boolean flexibleRightMargin) {
		this.flexibleRightMargin = flexibleRightMargin;
	}

	public boolean isFlexibleTopMargin() {
		return flexibleTopMargin;
	}

	public void setFlexibleTopMargin(boolean flexibleTopMargin) {
		this.flexibleTopMargin = flexibleTopMargin;
	}

	public boolean isFlexibleHeight() {
		return flexibleHeight;
	}

	public void setFlexibleHeight(boolean flexibleHeight) {
		this.flexibleHeight = flexibleHeight;
	}

	public boolean isFlexibleBottomMargin() {
		return flexibleBottomMargin;
	}

	public void setFlexibleBottomMargin(boolean flexibleBottomMargin) {
		this.flexibleBottomMargin = flexibleBottomMargin;
	}
}
