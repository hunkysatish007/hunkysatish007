package com.mobilous.mobileweb.resource;

public class Images {

	private String filename = "";
	private int id = -1;
	
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
